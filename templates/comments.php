<?php
if (post_password_required()) {
  return;
}
?>
<?php if (have_comments()) : ?>
  <section id="comments" class="comments">
    <h2><?php printf(_nx('One response to &ldquo;%2$s&rdquo;', '%1$s responses to &ldquo;%2$s&rdquo;', get_comments_number(), 'comments title', 'blueleaf'), number_format_i18n(get_comments_number()), '<span>' . get_the_title() . '</span>'); ?></h2>

    <ol class="comment-list">
      <?php wp_list_comments(['style' => 'ol', 'short_ping' => true]); ?>
    </ol>

    <?php if (get_comment_pages_count() > 1 && get_option('page_comments')) : ?>
      <nav>
        <ul class="pager">
          <?php if (get_previous_comments_link()) : ?>
            <li class="previous"><?php previous_comments_link(__('&larr; Older comments', 'blueleaf')); ?></li>
          <?php endif; ?>
          <?php if (get_next_comments_link()) : ?>
            <li class="next"><?php next_comments_link(__('Newer comments &rarr;', 'blueleaf')); ?></li>
          <?php endif; ?>
        </ul>
      </nav>
    <?php endif; ?>
  </section>
<?php endif; // have_comments() ?>

<section id="comment-form" class="comments">
  <?php if (!comments_open() && get_comments_number() != '0' && post_type_supports(get_post_type(), 'comments')) : ?>
    <div class="alert alert-warning">
      <?php _e('Comments are closed.', 'blueleaf'); ?>
    </div>
  <?php endif; ?>
  <?php comment_form(); ?>
</section>
